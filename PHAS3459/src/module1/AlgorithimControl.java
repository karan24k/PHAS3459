package module1;

public class AlgorithimControl {

	public static void main(String[] args) {
		int x = 1;
		int xmax = 17;
		for (x = 1 ; x < xmax; x++) {
		System.out.println("x = " +x);
		}
		
		{
		System.out.print("\n");
		}
		
		int y = 10;
		int ymin = -5;

		while( y >= ymin) {
		System.out.print("y = " +y );
		y--;
		System.out.print("\n");
		}
		
		{
		System.out.print("\n");
		}
		
		
		}
}
		
